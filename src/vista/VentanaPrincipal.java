    //Método que carga y actualiza las tablas de vehículos disponibles y alquilados
    public void agregaTablas (){
        tabla1 = new DefaultTableModel();
        tabla2 = new DefaultTableModel();
        tabla1.addColumn("Nombre"); tabla2.addColumn("Nombre");
        tabla1.addColumn("Matrícula"); tabla2.addColumn("Matrícula");
        tabla1.addColumn("Tipo"); tabla2.addColumn("Tipo");
        this.tablaDisponibles.setModel(tabla1);
        this.tablaAlquilados.setModel(tabla2);
        for (int i=0; i<AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos().size(); i++){
                String[] Datos = new String [3];
                Datos[0] = AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos().get(i).getNombre();
                Datos[1] = AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos().get(i).getMatricula();
                if (AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos().get(i) instanceof VTurismo) 
                    Datos[2] = "Vehículo Turismo";
                else Datos[2] = "Vehículo Camion";
            if (AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos().get(i).isAlquilado() == false){
                tabla1.addRow(Datos);
            }else {
                tabla2.addRow(Datos);
            }
        }  
    }
