//Devuelve un vehículo si este se encuentra alquilado, ademas muestra un JOptionPane con la informacion de la factura
    private void botonDevolverVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonDevolverVehiculoActionPerformed
        if (AgenciaAlquilaVehiculos.getVehiculo().buscarVehiculo(textMatricula.getText(), AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos()) == -1){
            JOptionPane.showMessageDialog(null, "METRÍCULA INGRESADA INEXISTENTE", "ERROR DE ENTRADA", JOptionPane.WARNING_MESSAGE);
        }else{
        if (AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos().get(AgenciaAlquilaVehiculos.getVehiculo().buscarVehiculo(textMatricula.getText(), AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos())).isAlquilado() == true){
            if (AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos().get(AgenciaAlquilaVehiculos.getVehiculo().buscarVehiculo(textMatricula.getText(), AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos())) instanceof VTurismo){
                VTurismo vt = new VTurismo ();
                vt = (VTurismo)AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos().get(AgenciaAlquilaVehiculos.getVehiculo().buscarVehiculo(textMatricula.getText(), AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos()));
                try{
                long nuevoKilometraje = Long.parseLong(JOptionPane.showInputDialog(null, "Ingrese nuevo registro del kilometraje"));
                if (nuevoKilometraje > vt.getKilometraje()){
                JOptionPane.showMessageDialog(null, "Total a Pagar: "+AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos().get(AgenciaAlquilaVehiculos.getVehiculo().buscarVehiculo(textMatricula.getText(), AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos())).devolverElVehiculo(AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos(), AgenciaAlquilaVehiculos.getVehiculo().buscarVehiculo(textMatricula.getText(), AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos()), nuevoKilometraje));
                vt.setKilometraje(nuevoKilometraje);
                vt.setAlquilado(false);
                AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos().set(AgenciaAlquilaVehiculos.getVehiculo().buscarVehiculo(textMatricula.getText(), AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos()), vt);
                }else{
                    JOptionPane.showMessageDialog(null, "Cifra ingresada incorrecta debe ser mayor a "+vt.getKilometraje() );
                }
                }catch (Exception asd){
                    JOptionPane.showMessageDialog(null, "Cifra ingresada incorrecta" );
                }
                
            }else{
                JOptionPane.showMessageDialog(null, "Total a Pagar: "+AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos().get(AgenciaAlquilaVehiculos.getVehiculo().buscarVehiculo(textMatricula.getText(), AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos())).devolverElVehiculo(AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos(), AgenciaAlquilaVehiculos.getVehiculo().buscarVehiculo(textMatricula.getText(), AgenciaAlquilaVehiculos.getVehiculo().getListaVehiculos()), 0));
            }
            agregaTablas ();
        }else{
            JOptionPane.showMessageDialog(null, "EL VEHÍCULO NO SE PUDO DEVOLVER", "ACCIÓN INVALIDADA", JOptionPane.WARNING_MESSAGE);
        }
        }
    }//GEN-LAST:event_botonDevolverVehiculoActionPerformed
