//Botón que agrega un nuevo vehículo si ingresan los campos correctamente
    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        AgenciaAlquilaVehiculos aux = new AgenciaAlquilaVehiculos();
        if (comboBoxTipoVehiculo.getSelectedIndex() == 0){
            vc = new VCamion ();
            if (this.textNombre.getText().equals("")){
                JOptionPane.showMessageDialog( this, "Por Favor Digite Nombre", "Registro", JOptionPane.INFORMATION_MESSAGE);
                this.textNombre.requestFocus();
            }else{
                if (textMatricula.getText().length() != 6){
                    JOptionPane.showMessageDialog( this, "Caracteres de la matrícula incompletos", "Registro", JOptionPane.INFORMATION_MESSAGE);
                    this.textMatricula.requestFocus();
                }else{
                    if (isNumeric(textMatricula.getText().substring(3)) && isLetter(textMatricula.getText().substring(0,3))) {
                        vc.setNombre(textNombre.getText());
                        vc.setMatricula(textMatricula.getText().toUpperCase());
                        aux.guardaVehiculos (vc);
                        dispose();
                    }else{
                        JOptionPane.showMessageDialog( this, "Formato de la matrícula incorrecto (AAA000)", "Registro", JOptionPane.INFORMATION_MESSAGE);
                        this.textMatricula.requestFocus();
                    }
                }}
            }else{
                if (comboBoxTipoVehiculo.getSelectedIndex() == 1){
                    vt = new VTurismo ();
                    if (this.textNombre.getText().equals("")){
                        JOptionPane.showMessageDialog( this, "Por Favor Digite Nombre", "Registro", JOptionPane.INFORMATION_MESSAGE);
                        this.textNombre.requestFocus();
                    }else{
                        if (textMatricula.getText().length() != 6){
                            JOptionPane.showMessageDialog( this, "Caracteres de la matrícula incompletos", "Registro", JOptionPane.INFORMATION_MESSAGE);
                            this.textMatricula.requestFocus();
                        }else{
                            if (isNumeric(textMatricula.getText().substring(3)) && isLetter(textMatricula.getText().substring(0,3))) {
                                vt.setNombre(textNombre.getText());
                                vt.setMatricula(textMatricula.getText().toUpperCase());
                                aux.guardaVehiculos (vt);
                                dispose();
                            }else{
                                JOptionPane.showMessageDialog( this, "Formato de la matrícula incorrecto (AAA000)", "Registro", JOptionPane.INFORMATION_MESSAGE);
                                this.textMatricula.requestFocus();
                            }
                        }}
                    }
                }
    }//GEN-LAST:event_botonAgregarActionPerformed
