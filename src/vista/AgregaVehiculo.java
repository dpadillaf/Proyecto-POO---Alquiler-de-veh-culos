    //Método tomado de: http://lineadecodigo.com/java/validar-si-un-dato-es-numerico-en-java/
    //Para saber si la cadena del parametro contiene solo numeros y retorna la respuesta true o false
    private static boolean isNumeric(String cadena){
        try {
            Integer.parseInt(cadena);
            return true;
        }catch (NumberFormatException nfe){
            return false;
        }
    }
 //Para saber si la cadena del parametro contiene solo letras y retorna la respuesta true o false   
    private static boolean isLetter (String cadena){
        cadena = cadena.toLowerCase();
   for(int i=0; i<cadena.length(); i++){
      if (!(cadena.charAt(i) >= 'a' && cadena.charAt(i) <= 'z') ){
         return false;
      }
   }
   return true;
}

